Zenbu Resto dapat anda temui di pintu masuk Matahari Shopping Center, Nagoya Hill Mall. Zenbu Resto ini baru beroperasi sekitar bulan Februari 2014 yang lalu. Pertama kali menjejalkan kaki menuju pintu depan, anda akan disambut oleh sahutan ‘Selamat Datang’ berbahasa Jepang dari waitress dan dekorasi interior restorannya yang sangat japanese. 
