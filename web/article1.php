<p>Didorong keinginan untuk berbagi sehat alami yang membahagiakan, tercetuslah nama Cheerful Cafe Vegan. Lusudirman, menggagas ide itu dalam bentuk sajian menu sehat alami vegetarian murni baik itu makanan maupun minuman. 
Terpancar dari nama cafe-nya cheerful yang berarti kebahagiaan dan vegan yang maknanya vegetarian murni, menu makanan dan minumannya jauh dari bahan-bahan kimia. Bahan yang diambil berfokus pada kacang-kacangan, seperti tahu, tempe, susu soya, sayuran serta buah-buahan.</p>
<hr>
<p>"Mengapa saya tak pakai susu biasa di pasaran? Karena susu tersebut pasti ada bahan kimianya sebagai pengawet, dan konsep vegan sendiri yaitu diatas vegetarian, jadi kami betul-betul murni memakai bahan dari sayuran, kacang-kacangan, buah, non daging, susu maupun telur. Siapa saja, dari kalangan manapun, bisa menikmati menu kami disini," ungkap Lusudirman.</p>
<hr>
<p>Dia tak hanya ingin memperkenalkan menu vegetarian murni saja kepada masyarakat, namun dia punya misi edukasi didalamnya kepada masyarakat Batam tentang pentingnya menjaga kesehatan berawal makanan dan minuman yang dikonsumsi sehari-hari. </p>
<hr>
<p>"Jangan ada anggapan tahu tempe adalah makanan kampung dan menyepelekan kasiatnya. Anda tahu sendiri kan tahu tempe terbuat dari kedelai kaya protein tanpa bahan kimia sama sekali dan tanpa pengawet, makanya semua makanan dan minuman yang akan saya gagas di cafe saya ini berbahan dasar tahu tempe. Sayuran dan buah-buahan murni pun saya ambil untuk bahan menu sajian kami," paparnya. </p>
<hr>
<p>Untuk menu andalan cafenya saat ini adalah Smoothie, yaitu campuran buah dengan kacang-kacangan. Dan seperti yang sudah dipaparkannya, bila menggunakan susu, dia hanya akan memakai susu soya ataupun susu kedelai. Di samping smoothie, Cheerful cafe juga menyediakan kopi.</p>
